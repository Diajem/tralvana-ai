def test_csv_rejects_unknown_columns_to_avoid_accidental_personal_data_import():
    with pytest.raises(CanonicalCommissionCsvError, match="unsupported columns: email"):
        load_canonical_commission_csv(StringIO(
            HEADER.rstrip("\n") + ",email\n"
            "expedia,Expedia UK,booking-123,,840.50,42.03,GBP,PENDING,EXPECTED,"
            "commission-123,2026-07-19T09:00:00Z,,,expedia,person@example.com\n"
        ))


@pytest.mark.parametrize(
    "replacement, message",
    (
        ("-1.00,42.03", "gross value must be non-negative"),
        ("840.50,42.001", "commission amount must be non-negative"),
        ("840.50,42.03,GB", "currency must be a three-letter ISO code"),
    ),
)
def test_csv_rejects_invalid_money_and_currency(replacement, message):
    prefix = "expedia,Expedia UK,booking-123,,"
    if replacement.endswith(",GB"):
        values = replacement + ",PENDING,EXPECTED"
    else:
        values = replacement + ",GBP,PENDING,EXPECTED"
    with pytest.raises(CanonicalCommissionCsvError, match=message):
        load_canonical_commission_csv(StringIO(
            HEADER
            + prefix + values
            + ",commission-123,2026-07-19T09:00:00Z,,,expedia\n"
        ))


def test_only_import_source_metadata_is_stored(reconciliation_database):
    with reconciliation_database.begin() as session:
        repository = SqlAlchemyCommercialRepository(session)
        CommissionReconciliationService(repository).reconcile(_rows())
        partner = repository.get_partner_by_slug("expedia")
        programme = repository.get_programme_by_partner_and_name(partner.id, "Expedia UK")
        conversion = repository.get_conversion_by_external_reference(
            programme.id, "booking-123"
        )
        assert conversion.conversion_metadata == {"import_source": "expedia"}
