HEADER = (
    "partner_slug,programme_name,transaction_reference,click_sub_id,gross_value,"
    "commission_amount,currency,conversion_status,commission_status,"
    "commission_reference,booked_at,approved_at,paid_at,source\n"
)


@pytest.fixture
def reconciliation_database(tmp_path: Path):
    engine = create_engine_from_url(f"sqlite+pysqlite:///{tmp_path / 'reconciliation.db'}")
    Base.metadata.create_all(engine)
    factory = create_session_factory(engine)
    with factory.begin() as session:
        repository = SqlAlchemyCommercialRepository(session)
        ledger = CommercialLedgerService(repository)
        partner = ledger.register_partner(
            slug="expedia",
            name="Expedia",
            website_url="https://www.expedia.com",
        )
        programme = ledger.register_programme(
            partner_id=partner.id,
            name="Expedia UK",
            vertical=CommercialVertical.ACCOMMODATION,
            tracking_template="https://www.expedia.com/?aff={affiliate_id}",
            affiliate_identifier="mZObD29",
            default_currency="GBP",
        )
        click = ledger.record_click(
            programme_id=programme.id,
            destination_url="https://www.expedia.com/Hotel-Search",
            tracking_url="https://www.expedia.com/?aff=mZObD29",
        )
        # Reconciliation uses the click sub-ID generated by the safe-link path.
        click.sub_id = "trip-42"
        row = session.get(OutboundClickRow, click.id)
        row.sub_id = click.sub_id
    yield factory
    engine.dispose()


def _rows(
    *,
    conversion_status: str = "PENDING",
    commission_status: str = "EXPECTED",
    commission_amount: str = "42.03",
    click_sub_id: str = "trip-42",
):
    return load_canonical_commission_csv(StringIO(
        HEADER
        + f"expedia,Expedia UK,booking-123,{click_sub_id},840.50,{commission_amount},GBP,"
        + f"{conversion_status},{commission_status},commission-123,"
        + "2026-07-19T09:00:00Z,,,expedia\n"
    ))


def test_reconciliation_creates_conversion_commission_and_matches_click(
    reconciliation_database,
):
    with reconciliation_database.begin() as session:
        repository = SqlAlchemyCommercialRepository(session)
        summary = CommissionReconciliationService(repository).reconcile(_rows())
        assert summary.successful == 1
        assert summary.conversions_created == 1
        assert summary.commissions_created == 1
        assert summary.clicks_matched == 1
        assert repository.counts()["conversions"] == 1
        assert repository.counts()["commissions"] == 1


def test_repeated_report_is_idempotent_and_updates_status(reconciliation_database):
    with reconciliation_database.begin() as session:
        repository = SqlAlchemyCommercialRepository(session)
        service = CommissionReconciliationService(repository)
        service.reconcile(_rows())
        summary = service.reconcile(_rows(
            conversion_status="CONFIRMED",
            commission_status="APPROVED",
            commission_amount="50.00",
        ))
        assert summary.conversions_updated == 1
        assert summary.commissions_updated == 1
        assert repository.counts()["conversions"] == 1
        assert repository.counts()["commissions"] == 1
        partner = repository.get_partner_by_slug("expedia")
        programme = repository.get_programme_by_partner_and_name(partner.id, "Expedia UK")
        conversion = repository.get_conversion_by_external_reference(
            programme.id, "booking-123"
        )
        commission = repository.get_commission_by_external_reference("commission-123")
        assert conversion.status == ConversionStatus.CONFIRMED
        assert commission.status == CommissionStatus.APPROVED
        assert str(commission.amount) == "50.00"


