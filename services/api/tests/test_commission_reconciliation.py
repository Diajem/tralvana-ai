def test_terminal_paid_commission_cannot_regress(reconciliation_database):
    with reconciliation_database.begin() as session:
        service = CommissionReconciliationService(SqlAlchemyCommercialRepository(session))
        service.reconcile(_rows(
            conversion_status="CONFIRMED",
            commission_status="PAID",
        ))
        summary = service.reconcile(_rows(
            conversion_status="CONFIRMED",
            commission_status="EXPECTED",
        ))
        assert summary.successful == 0
        assert summary.issues[0].message == "terminal commission status cannot be regressed"


